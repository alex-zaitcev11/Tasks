public class Task_String4 {
    public static void main(String[] args) {
        String tes = "Red is favorite colour";
        String la = "Orange is also my favorite colour";
        boolean f1 = tes.startsWith("Red");
        boolean f6 = la.startsWith("Orange");
        System.out.println(tes + " " + f1);
        System.out.println(la + " " + f6);
    }
}
